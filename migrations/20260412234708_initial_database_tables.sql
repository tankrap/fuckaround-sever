CREATE TABLE IF NOT EXISTS "people" (
	"id" TEXT NOT NULL,
	"username" VARCHAR(32) NOT NULL,
	"email" TEXT NOT NULL,
	"time_created" TIMESTAMPTZ NOT NULL,
	"last_updated" TIMESTAMPTZ,
	"flags" BIGINT NOT NULL DEFAULT 0,
	"first_name" VARCHAR(255) NOT NULL,
	"last_name" VARCHAR(255) NOT NULL,
	"email_verified" BOOLEAN NOT NULL DEFAULT FALSE,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "structure" (
	"player" TEXT NOT NULL,
	"id" TEXT NOT NULL,
	"bought_when" TIMESTAMPTZ NOT NULL,
	"meta" BIGINT NOT NULL,
	"max_units" BIGINT,
	"min_units" BIGINT,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "seed" (
	"creator" TEXT NOT NULL,
	"id" TEXT NOT NULL,
	"original_structure" TEXT NOT NULL,
	"name" VARCHAR(32) NOT NULL UNIQUE,
	"gained_when" TIMESTAMPTZ NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "structure_meta" (
	"id" BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	"model" TEXT NOT NULL,
	"cost" NUMERIC,
	"flags" BIGINT NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "player" (
	"id" TEXT NOT NULL,
	"person" TEXT NOT NULL,
	"cash" NUMERIC NOT NULL,
	"name" VARCHAR(32) NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "bank" (
	"id" BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	"name" TEXT NOT NULL,
	"routing_num" TEXT NOT NULL,
	PRIMARY KEY("id", "name")
);




CREATE TABLE IF NOT EXISTS "bank_account" (
	"person" TEXT NOT NULL,
	"balance" NUMERIC NOT NULL,
	"opened_when" TIMESTAMPTZ NOT NULL,
	"last_transaction" TIMESTAMPTZ,
	"bank" BIGINT,
	"routing_num" TEXT NOT NULL,
	"account_num" TEXT,
	PRIMARY KEY("person")
);




CREATE TABLE IF NOT EXISTS "transaction" (
	"id" TEXT NOT NULL,
	"to_acct" TEXT NOT NULL,
	"from_acct" TEXT NOT NULL,
	"to_routing" TEXT NOT NULL,
	"from_routing" TEXT NOT NULL,
	"transacted_when" TIMESTAMPTZ NOT NULL,
	"amount" NUMERIC NOT NULL,
	"memo" VARCHAR(512),
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "cash_transaction" (
	"id" TEXT NOT NULL,
	"from" TEXT NOT NULL,
	"to" TEXT NOT NULL,
	"amount" NUMERIC NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "planter" (
	"id" TEXT NOT NULL,
	"structure" TEXT NOT NULL,
	"player" TEXT NOT NULL,
	"type" INTEGER NOT NULL,
	"level" INTEGER NOT NULL,
	"meta" BIGINT,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "planter_meta" (
	"id" BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	"model" TEXT NOT NULL,
	"cost" NUMERIC NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "plots" (
	"id" TEXT NOT NULL,
	"planted_seed" TEXT NOT NULL,
	"planted_by" TEXT NOT NULL,
	"time_planted" TIMESTAMPTZ NOT NULL,
	"growing_plant" TEXT NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "previous_planted" (
	"id" INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	"plot" TEXT NOT NULL,
	"seed" TEXT NOT NULL,
	"planted_when" TIMESTAMPTZ NOT NULL,
	"planted_by" TEXT NOT NULL,
	"plant" TEXT,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "plant" (
	"id" TEXT NOT NULL,
	"seed" TEXT NOT NULL,
	"phase" INTEGER NOT NULL,
	"meta" BIGINT NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "plant_meta" (
	"id" BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	"model" TEXT NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "in_storage" (
	"id" TEXT NOT NULL,
	"resource" TEXT NOT NULL,
	"structure" TEXT NOT NULL,
	"stored_by" TEXT NOT NULL,
	"stored_when" TIMESTAMPTZ NOT NULL,
	"removed_when" TIMESTAMPTZ NOT NULL,
	"in_storage" BOOLEAN NOT NULL DEFAULT FALSE,
	PRIMARY KEY("id")
);



